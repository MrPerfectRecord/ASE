def main():
    """Generate SVG paths and output TypeScript file."""
    print("Generating high-quality US state boundaries...")

    output_lines = ["export const S: Record<string,[string,number,number]> = {"]

    state_order = sorted(STATE_BOUNDARIES.keys())

    for state in state_order:
        coords = STATE_BOUNDARIES[state]
        path = coords_to_path(coords)
        cx, cy = centroid(coords)

        # Escape any problematic characters in path
        path_escaped = path.replace('"', '\\"')

        output_lines.append(f'  {state}: ["{path_escaped}",{cx},{cy}],')

    output_lines.append("};")

    typescript_code = "\n".join(output_lines)

    # Create directory if needed
    import os
    output_dir = "/sessions/gallant-amazing-bell/mnt/ASE/ase-website/src/data"
    os.makedirs(output_dir, exist_ok=True)

    # Write to output file
    output_path = os.path.join(output_dir, "states.ts")
    with open(output_path, "w") as f:
        f.write(typescript_code)

    print(f"TypeScript output written to: {output_path}")
    print(f"Generated {len(state_order)} states/territories")

    # Generate SVG preview
    svg_lines = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 960 600" width="1920" height="1200">',
        '<style>',
        '.state { fill: #e8f4f8; stroke: #333; stroke-width: 0.5; }',
        '.state:hover { fill: #b3d9e6; }',
        '.label { font-size: 10px; text-anchor: middle; pointer-events: none; }',
        '</style>',
        '<rect width="960" height="600" fill="#fff"/>',
    ]

    for state in state_order:
        coords = STATE_BOUNDARIES[state]
        path = coords_to_path(coords)
        cx, cy = centroid(coords)

        path_escaped = path.replace('"', '\\"')
        svg_lines.append(f'<path class="state" d="{path_escaped}"/>')
        svg_lines.append(f'<text class="label" x="{cx}" y="{cy}">{state}</text>')

    svg_lines.append('</svg>')

    svg_content = "\n".join(svg_lines)
    svg_path = "/sessions/gallant-amazing-bell/map_hq_preview.svg"
    with open(svg_path, "w") as f:
        f.write(svg_content)

    print(f"SVG preview written to: {svg_path}")


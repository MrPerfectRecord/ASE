"""
High-quality US state boundary SVG generator using Albers equal-area conic projection.
Generates detailed paths with 100+ points for complex coastal states.
"""

import math
from typing import List, Tuple, Dict

# Albers projection parameters
LON0, LAT0 = -96.0, 37.5
PHI1, PHI2 = 29.5, 45.5
SCALE = 1070
TRANSLATE_X, TRANSLATE_Y = 480, 350

def albers_projection(lon: float, lat: float) -> Tuple[float, float]:
    """Apply Albers equal-area conic projection with SVG Y-axis flip."""
    lon0_rad = math.radians(LON0)
    lat0_rad = math.radians(LAT0)
    phi1 = math.radians(PHI1)
    phi2 = math.radians(PHI2)

    phi = math.radians(lat)
    lam = math.radians(lon)

    n = 0.5 * (math.sin(phi1) + math.sin(phi2))
    C = math.cos(phi1)**2 + 2*n*math.sin(phi1)
    rho0 = math.sqrt(C - 2*n*math.sin(lat0_rad)) / n

    theta = n * (lam - lon0_rad)
    rho = math.sqrt(C - 2*n*math.sin(phi)) / n

    x = rho * math.sin(theta)
    y = rho0 - rho * math.cos(theta)

    px = round(x * SCALE + TRANSLATE_X, 1)
    py = round(-y * SCALE + TRANSLATE_Y, 1)  # FLIP Y for SVG

    return (px, py)

def coords_to_path(coordinates: List[Tuple[float, float]]) -> str:
    """Convert list of (lon, lat) tuples to SVG path string."""
    if not coordinates:
        return ""

    path_parts = []
    for i, (lon, lat) in enumerate(coordinates):
        px, py = albers_projection(lon, lat)
        if i == 0:
            path_parts.append(f"M{px},{py}")
        else:
            path_parts.append(f"L{px},{py}")

    path_parts.append("Z")
    return "".join(path_parts)

def centroid(coordinates: List[Tuple[float, float]]) -> Tuple[float, float]:
    """Calculate centroid of coordinates in projected space."""
    if not coordinates:
        return (0, 0)

    projected = [albers_projection(lon, lat) for lon, lat in coordinates]
    cx = sum(p[0] for p in projected) / len(projected)
    cy = sum(p[1] for p in projected) / len(projected)
    return (round(cx, 1), round(cy, 1))

